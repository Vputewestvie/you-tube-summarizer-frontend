const CHANNELS = ["Разбор по полочкам", "Кратко о главном", "TechТалк", "Мысли вслух"]
const DURATIONS = ["12:47", "24:15", "8:32", "41:09", "17:53"]

function hashString(input: string): number {
  let hash = 0
  for (let i = 0; i < input.length; i++) {
    hash = (hash << 5) - hash + input.charCodeAt(i)
    hash |= 0
  }
  return Math.abs(hash)
}

const TITLES = [
  "Как построить систему привычек, которая реально работает",
  "Инвестиции с нуля: что нужно знать до первого вклада",
  "Как на самом деле работают языковые модели",
]

export function generateSummary(url: string): SummaryResult {
  const videoId = extractVideoId(url)
  const seed = hashString(videoId ?? url)
  const base = SAMPLE_SUMMARIES[seed % SAMPLE_SUMMARIES.length]

  return {
    video: {
      title: TITLES[seed % TITLES.length],
      channel: CHANNELS[seed % CHANNELS.length],
      duration: DURATIONS[seed % DURATIONS.length],
      thumbnail: videoId
        ? `https://i.ytimg.com/vi/${videoId}/hqdefault.jpg`
        : "",
    },
    ...base,
  }
}

export function fakeSummarize(url: string): Promise<SummaryResult> {
  return new Promise((resolve) => {
    // Имитация сетевой задержки обработки на «бэкенде».
    setTimeout(() => resolve(generateSummary(url)), 6500)
  })
}
